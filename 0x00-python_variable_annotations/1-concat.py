#!/usr/bin/env python3
"""1. Basic annotations - concat"""


def concat(str1: str, str2: str) -> str:
    """Takes two strings as arguments and
    returns a concatenated string

    Parameters
    ----------
    str1 : str
        string
    str1 : str
        string

    Returns
    -------
    str
        concatenated string
    """
    return f"{str1} {str2}"

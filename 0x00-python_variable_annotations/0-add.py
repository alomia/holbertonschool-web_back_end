#!/usr/bin/env python3
"""0. Basic annotations - add"""


def add(a: float, b: float) -> float:
    """takes as arguments a float a and a float b
    and returns their sum as a float.

    Parameters
    ----------
    a : float
        number
    b : float
        number

    Returns
    -------
    float
        the sum of a and b
    """
    return a + b
